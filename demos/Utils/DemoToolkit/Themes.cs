  public sealed class Palette
  {
    public Palette(string fill, string stroke, string nodeLabelFill, string edgeLabelFill, string text) {
      Fill = BrushFromHex(fill);
      Stroke = BrushFromHex(stroke);
      NodeLabelFill = BrushFromHex(nodeLabelFill);
      EdgeLabelFill = BrushFromHex(edgeLabelFill);
      Text = BrushFromHex(text);
    }

    public Brush Fill { get; private set; }

    public Brush Stroke { get; private set; }

    public Brush NodeLabelFill { get; private set; }

    public Brush EdgeLabelFill { get; private set; }

    public Brush Text { get; private set; }

    private static SolidColorBrush BrushFromHex(string hexColorString) {
      return (SolidColorBrush) new BrushConverter().ConvertFrom(hexColorString);
    }
  }
